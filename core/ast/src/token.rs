#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Token {
    pub tag: TokenTag,
    pub kind: TokenKind,
    pub span: Span,
    pub leading_line_break: bool,
    pub escaped: bool,
}

impl Token {
    pub const fn tag(&self) -> TokenTag {
        self.tag
    }
}

impl fmt::Display for TokenTag {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "{self:?}")
    }
}

impl fmt::Display for TokenKind {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::Number(value) => write!(f, "{value}"),
            Self::String(value) => write!(f, "\"{value}\""),
            Self::Template { value, .. } => write!(f, "`{value}`"),
            Self::Identifier(name) => write!(f, "{name}"),
            Self::PrivateName(name) => write!(f, "#{name}"),
            Self::RegExp { body, flags } => write!(f, "/{body}/{flags}"),
            kind => {
                if let Some(keyword) = kind.keyword_text() {
                    write!(f, "{keyword}")
                } else {
                    write!(f, "{:?}", kind.tag())
                }
            }
        }
    }
}
